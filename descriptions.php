<h2 class="top-header">Why Are National Parks Important</h2>
<p>National parks protect the best of our natural heritage: stunning landscapes, extraordinary wildlife and majestic forests.Together with other protected areas they form the basis
    of our economic and social wellbeing, attract millions of visitors annually, and help to protect Australia’s unique wildlife by acting as a refuge for threatened species.
    Although their primary purpose is the protection of biodiversity, National Parks also deliver other invaluable economic, social,
    cultural and health benefits to Australians. Future generations deserve the right to see these natural values intact and protected as we do today.</p>
<h2>Natural Values</h2>
<p>Our national parks form the cornerstone of biodiversity conservation in Australia, containing vital habitat that provides safe havens in which animals and plants can survive and thrive.
    Together with other protected areas, they provide a ‘backbone’ of core conservation areas that can be linked by conservation efforts across different tenures, supporting a diverse, healthy and resilient environment.
    A well-connected landscape is essential for saving NSW’s 1,000 threatened species, 70% of which occur in our national parks.
    In addition our protected areas provide life-sustaining services vital for the wellbeing of our environment and society, such as protection of urban water catchments and climate amelioration.</p>
<h2>Economic Values</h2>
<p>National Parks provide a major boost to Australia’s economy, with nature-based tourism bringing $23 billion into the country every year.
    Regional communities in particular benefit from the 35.5 million people who visit NSW’s national parks each year, through job creation and money spent on accommodation, fuel and food.
    The Great Barrier Reef alone attracts more than $6 billion a year in tourist-spending and supports over 63,000 jobs.
    Intact natural areas also provide a variety of resources and processes vital for human life and the economy. For example protected areas provide a water catchment and filtration service,
    improving the quality of the water we use for drinking,
    agriculture and industry. By one analysis these ‘ecosystem services’ are worth US$33 trillion a year.</p>

<h2>Social and cultural values</h2>
<p>NSW’s national parks are alive with history and culture.
    From ancient aboriginal rock-art sites, to the buildings left over from early European settlements,
    our national parks serve as a natural history book dating back thousands of years. Our national parks
    protect these vital and fragile places; areas where the traces of a history extending back more than 22,000
    years can remain undisturbed. Today our protected areas are still used by Aboriginal people in a number of ways,
    such as a source of food, tools, medicine and trade, as well as in ceremonial and spiritual activities. In this way
    national parks provide an opportunity in for people to establish and continue their connections with the land and to share
    their irreplaceable cultural knowledge with the wider community.</p>
<h2>Health and Wellbeing</h2>
<p>Natural areas have a profound effect on our physical and emotional health and wellbeing.
    In our increasingly frenetic world, our national parks are important sanctuaries where people can take time out,
    enjoy nature, get fit, relax and revitalise, whilst nature’s inherent beauty serves as a source of artistic,
    creative and spiritual inspiration. Research on the benefits of contact with the natural environment show that it is likely to have a significant positive psychological effect,
    serving to reduce stress, anger, frustration and aggression, providing an opportunity for social bonding, and serving as a place for learning and mental stimulation.
    Children in particular display long-term benefits of playing outdoors. In addition to the obvious health and wellbeing benefits our national parks bring, they also assist us in less obvious ways,
    such as acting as natural buffers against extreme weather events, helping to control our climate, providing us with clean water,
    improving food security and serving as an important resource for the pharmaceutical industry.</p>
